package com.unisound.unicar.gui.model;

/**
 * 
 * @author xiaodong
 * @date 20150713
 */
public class FunctionHelpTextInfo {

    private String type;

    private String helpText;

    private boolean isOfflineText;

    public FunctionHelpTextInfo() {

    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getHelpText() {
        return helpText;
    }

    public void setHelpText(String helpText) {
        this.helpText = helpText;
    }

    public boolean isOfflineText() {
        return isOfflineText;
    }

    public void setOfflineText(boolean isOfflineText) {
        this.isOfflineText = isOfflineText;
    }


}
