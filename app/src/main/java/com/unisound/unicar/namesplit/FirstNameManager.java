    private void loadFirstNames() {
        // 从FIRST_NAME_FILE中加载姓氏到_firstNameSet中
        BufferedReader firstNameBufferReader = null;
        try {
            File file = new File("");
            firstNameBufferReader = new BufferedReader(new FileReader(file));
            String firstName = null;
            while (true) {
                firstName = firstNameBufferReader.readLine();
                if (firstName == null) break;
                if (this.firstNameSet.contains(firstName)) continue;
                this.firstNameSet.add(firstName);
            }
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        } catch (IOException e) {
            e.printStackTrace();
        } finally // 关闭文件
        {
            try {
                firstNameBufferReader.close();
            } catch (IOException e) {
                e.printStackTrace();
            }
        }
    }

    public List<String> GetFirstNames(String name) {
        List<String> firstNames = new ArrayList<String>();

        if ((name == null) || (name.trim().equals(""))) return firstNames;

        String firstName = name.substring(0, 1);
        if (mSingleLastNameMap.containsValue(firstName)) firstNames.add(firstName);

        if (name.length() < 2) return firstNames;
        firstName = name.substring(0, 2);
        if (mMutipleLastNameMap.containsValue(firstName)) firstNames.add(firstName);

        return firstNames;
    }

